#include "ISaviorEditor.h"
#include "SaviorEditor.h"
#include "SaviorStyle.h"

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#define LOCTEXT_NAMESPACE "Synaptech"

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class FSaviorEditor : public ISaviorEditor {
public:
	virtual void StartupModule() override {
		IAssetTools& AssetTools = FModuleManager::LoadModuleChecked<FAssetToolsModule>("AssetTools").Get();
		SY_AssetCategory = AssetTools.RegisterAdvancedAssetCategory(FName(TEXT("Synaptech")),LOCTEXT("SynaptechCategory","Synaptech"));
		//
		{
			TSharedRef<IAssetTypeActions> ATA_Savior = MakeShareable(new FATA_Savior);
			AssetTools.RegisterAssetTypeActions(ATA_Savior);
		}
		//
		FSaviorStyle::Initialize();
	}
};

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#undef LOCTEXT_NAMESPACE

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

IMPLEMENT_MODULE(FSaviorEditor,SaviorEditor);

